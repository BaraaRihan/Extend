package Create;

public class User {



        private String name;
        private String Email;

        public User() {
        }

        public User(String name, String Email) {
            this.name = name;
            this.Email = Email;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public String getEmail() {
            return Email;
        }

        public void setJob(String Email) {
            this.Email = Email;
        }
    }


